namespace Calculadora
{
    public class CalculadoraSimples
    {
        public int Adicionar(int x, int y)
        {
            return x + y;
        }

        public int Multipicacao(int x, int y)
        {
            return x * y;
        }
        
        public int Divisao(int x, int y)
        {
            return x / y;
        }

        public int Substracao(int x, int y)
        {
            return x - y;
        }


        public int Resto(int x, int y)
        {
            return x % y;
        }

    }
}
